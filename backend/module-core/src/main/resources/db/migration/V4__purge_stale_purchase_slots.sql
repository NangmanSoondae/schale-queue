-- ============================================================================
--  V4 — 비활성 주문의 구매 슬롯 일괄 반납 (리뷰 M7 후속 데이터 정리)
--  ----------------------------------------------------------------------------
--  슬롯(purchase_slot)의 시맨틱이 "활성(PENDING) 주문 1건"으로 재정의되면서
--  (확정/취소/만료 시 반납), 과거 시맨틱(확정 후에도 보존) 아래 쌓인 기존 행 중
--  주문이 이미 PENDING 을 벗어난 것들은 잔재가 되어 재구매를 영구 차단한다.
--  코드 배포 시점에 한 번 정리한다. 누적 수량 한도는 이제 주문 이력 합산이 담당하므로
--  슬롯 삭제가 P-O3 한도를 약화시키지 않는다.
-- ============================================================================
DELETE ps
  FROM purchase_slot ps
  JOIN orders o ON o.id = ps.order_id
 WHERE o.order_status <> 'PENDING';
