-- ============================================================================
--  V5 — 상품별 결제 타임아웃 컬럼 (P-O2 구현, 리뷰 잔여 '문서-코드 드리프트' 해소)
--  ----------------------------------------------------------------------------
--  문서(11_domain_policy P-O2)는 "Goods.paymentTimeoutMinutes(기본 10분, 1~30) 상품별
--  설정"으로 확정돼 있었으나 코드는 5분 고정 상수였다. 컬럼을 추가해 문서를 구현으로 맞춘다.
--  DB DEFAULT 는 Hibernate 가 INSERT 에 항상 컬럼을 포함해 적용되지 않으므로(M6 교훈),
--  기본값 강제는 엔티티 빌더가 담당하고 레거시 NULL 행은 읽기 측 기본(10분)으로 해석한다.
-- ============================================================================
ALTER TABLE goods
    ADD COLUMN payment_timeout_minutes INT NULL DEFAULT 10
        COMMENT '결제창 수명(분, P-O2). NULL=기본 10분. 허용 1~30'
        AFTER max_purchase_per_member;
